    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed

        customer = bookCustomerCombo.getSelectedItem().toString();
        flight = bookFlightCombo.getSelectedItem().toString();
        date = Date.valueOf(bookDateCombo.getSelectedItem().toString());
        if (Flight.getSeats(flight)>Booking.getNumBookedSeats(flight, date))
        {
            currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
            Booking.createBooking(customer, flight, date, currentTimestamp.toString());
            outBook.setText(customer + " has been successfully booked for flight " + flight + " on " + date + ". Thank you for choosing Fly-By-Night.");
        }
        else
        {
            currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
            Waitlist.createWaitList(customer, flight, date, currentTimestamp);
            outBook.setText(customer + " has been added to the waitlist for flight " + flight + " on " + date +". If a seat becomes available, it will be given to you according to your waitlist number.");
        }
    }//GEN-LAST:event_bookButtonActionPerformed

