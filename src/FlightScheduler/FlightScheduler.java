    private void cancelBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBookingActionPerformed
        String dropResult;
        String name = cancelNameField.getText();
        String date = cancelDateCombo.getSelectedItem().toString();
        dropResult = Booking.cancelBooking(name,date);
        this.updateComboBoxes();
        if(dropResult=="")
            outCancel.setText("Error \n You are not on this flight. Please check your flight number.");
        if(dropResult=="waitlist")
            outCancel.setText(name + " has successfully been removed from the waitlist for flight on " + date);
        
        if(dropResult=="bookings")
            outCancel.setText("Booking for " + name + " on " + date + " has sucessfully been cancelled.");
    }//GEN-LAST:event_cancelBookingActionPerformed

